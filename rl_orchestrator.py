# ==========================================
# 1. CONFIGURATION
# ==========================================
SUBSTRATE_NODES = 15
SUBSTRATE_CPU_CAP = 100
SUBSTRATE_LINK_BW = 100
MAX_VNR_NODES = 4
NUM_ALTERNATIVES = 2
TRAINING_STEPS = 25000
TARGET_REQS = 200             # Fair Evaluation Target

# ==========================================
# 2. DATA GENERATOR
# ==========================================
class GraphGenerator:
    @staticmethod
    def generate_substrate(num_nodes=SUBSTRATE_NODES):
        G = nx.barabasi_albert_graph(num_nodes, 3)
        for u in G.nodes:
            G.nodes[u]['cpu'] = np.random.randint(50, SUBSTRATE_CPU_CAP)
            G.nodes[u]['max_cpu'] = G.nodes[u]['cpu']
        for u, v in G.edges:
            G.edges[u, v]['bw'] = np.random.randint(50, SUBSTRATE_LINK_BW)
            G.edges[u, v]['max_bw'] = G.edges[u, v]['bw']
        return G

    @staticmethod
    def generate_request_alternatives():
        """
        Alt 1: Compute Heavy (High CPU, Low BW)
        Alt 2: Bandwidth Heavy (Low CPU, High BW)
        """
        num_nodes = np.random.randint(3, MAX_VNR_NODES + 1)
        base = nx.path_graph(num_nodes)

        # Alt 1: Compute Heavy
        alt1 = base.copy()
        for u in alt1.nodes:
            alt1.nodes[u]['cpu'] = np.random.randint(20, 30)
        for u, v in alt1.edges:
            alt1.edges[u, v]['bw'] = np.random.randint(5, 10)

        # Alt 2: Bandwidth Heavy
        alt2 = base.copy()
        for u in alt2.nodes:
            alt2.nodes[u]['cpu'] = np.random.randint(5, 10)
        for u, v in alt2.edges:
            alt2.edges[u, v]['bw'] = np.random.randint(20, 30)

        return [alt1, alt2]

# ==========================================
# 3. ENVIRONMENT
# ==========================================
class VNEAPEnv(gym.Env):
    def __init__(self):
        super().__init__()
        self.substrate = GraphGenerator.generate_substrate()
        self.initial_substrate = self.substrate.copy()

        self.total_cpu = sum(nx.get_node_attributes(self.substrate, 'max_cpu').values())
        self.total_bw = sum(nx.get_edge_attributes(self.substrate, 'max_bw').values()) * 2

        self.observation_space = spaces.Dict({
            "substrate_cpu": spaces.Box(low=0, high=SUBSTRATE_CPU_CAP, shape=(SUBSTRATE_NODES,), dtype=np.float32),
            "request_state": spaces.Box(low=0, high=100, shape=(NUM_ALTERNATIVES, MAX_VNR_NODES), dtype=np.float32),
            "phase_indicator": spaces.Discrete(2) # 0=Selection, 1=Mapping
        })

        self.action_space = spaces.Discrete(SUBSTRATE_NODES)

        self.current_alts = []
        self.selected_alt_idx = None
        self.mapping_phase = False
        self.current_v_node_idx = 0
        self.mapped_nodes = {}
        self.active_deployments = []

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        self.substrate = GraphGenerator.generate_substrate()
        self.total_cpu = sum(nx.get_node_attributes(self.substrate, 'max_cpu').values())
        bw_caps = nx.get_edge_attributes(self.substrate, 'max_bw')
        self.total_bw = sum(bw_caps.values()) if bw_caps else 1

        self.current_alts = GraphGenerator.generate_request_alternatives()

        self.mapping_phase = False
        self.selected_alt_idx = None
        self.current_v_node_idx = 0
        self.mapped_nodes = {}
        self.active_deployments = []

        return self._get_obs(), {}

    def _get_obs(self):
        sub_cpu = np.array([self.substrate.nodes[n]['cpu'] for n in range(SUBSTRATE_NODES)], dtype=np.float32)
        req_matrix = np.zeros((NUM_ALTERNATIVES, MAX_VNR_NODES), dtype=np.float32)
        for i, alt in enumerate(self.current_alts):
            for j, node in enumerate(alt.nodes):
                if j < MAX_VNR_NODES:
                    req_matrix[i][j] = alt.nodes[node]['cpu']

        return {
            "substrate_cpu": sub_cpu,
            "request_state": req_matrix,
            "phase_indicator": 1 if self.mapping_phase else 0
        }

    def get_utilization(self):
        current_cpu = sum(nx.get_node_attributes(self.substrate, 'cpu').values())
        used_cpu = self.total_cpu - current_cpu
        current_bw = sum(nx.get_edge_attributes(self.substrate, 'bw').values())
        used_bw = self.total_bw - current_bw if self.total_bw > 0 else 0
        return (used_cpu / self.total_cpu) * 100, (used_bw / self.total_bw) * 100

    def action_masks(self):
        mask = np.zeros(SUBSTRATE_NODES, dtype=bool)
        if not self.mapping_phase:
            for i in range(len(self.current_alts)):
                mask[i] = True
        else:
            needed_cpu = self.current_alts[self.selected_alt_idx].nodes[self.current_v_node_idx]['cpu']
            for i in range(SUBSTRATE_NODES):
                if self.substrate.nodes[i]['cpu'] >= needed_cpu:
                    if i not in self.mapped_nodes.values():
                        mask[i] = True
        return mask

    def _map_links(self, graph, mapping):
        allocated_paths = []
        for u, v in graph.edges:
            p_u, p_v = mapping[u], mapping[v]
            bw_needed = graph.edges[u, v]['bw']

            try:
                path = nx.shortest_path(self.substrate, source=p_u, target=p_v, weight=None)
                for i in range(len(path) - 1):
                    s, d = path[i], path[i+1]
                    if self.substrate[s][d]['bw'] < bw_needed:
                        raise nx.NetworkXNoPath

                for i in range(len(path) - 1):
                    s, d = path[i], path[i+1]
                    self.substrate[s][d]['bw'] -= bw_needed
                    allocated_paths.append((s, d, bw_needed))

            except nx.NetworkXNoPath:
                for s, d, amt in allocated_paths:
                    self.substrate[s][d]['bw'] += amt
                return False, []
        return True, allocated_paths

    def step(self, action):
        if isinstance(action, (np.ndarray, np.generic)):
            action = int(action)

        reward = 0
        done = False
        info = {}

        # Departure Process
        if self.active_deployments and np.random.rand() < 0.15:
            dep = self.active_deployments.pop(0)
            for p_node, cpu_amt in dep['nodes']:
                self.substrate.nodes[p_node]['cpu'] = min(self.substrate.nodes[p_node]['max_cpu'],
                                                          self.substrate.nodes[p_node]['cpu'] + cpu_amt)
            for u, v, bw_amt in dep['links']:
                self.substrate[u][v]['bw'] = min(self.substrate[u][v]['max_bw'],
                                                 self.substrate[u][v]['bw'] + bw_amt)

        if not self.mapping_phase:
            self.selected_alt_idx = action
            self.mapping_phase = True
        else:
            p_node = action
            v_node = self.current_v_node_idx
            selected_graph = self.current_alts[self.selected_alt_idx]
            cpu_demand = selected_graph.nodes[v_node]['cpu']

            if self.substrate.nodes[p_node]['cpu'] < cpu_demand:
                reward = -50
                info['msg'] = "Mapping Failed (CPU)"
                self._reset_request_state()
            else:
                self.substrate.nodes[p_node]['cpu'] -= cpu_demand
                self.mapped_nodes[v_node] = (p_node, cpu_demand)
                reward = -(0.05 * cpu_demand)

                self.current_v_node_idx += 1

                if self.current_v_node_idx >= len(selected_graph.nodes):
                    simple_map = {v: p[0] for v, p in self.mapped_nodes.items()}
                    success, path_data = self._map_links(selected_graph, simple_map)

                    if success:
                        reward += 50
                        info['msg'] = "Fully Embedded"
                        self.active_deployments.append({
                            'nodes': list(self.mapped_nodes.values()),
                            'links': path_data
                        })
                    else:
                        reward = -20
                        info['msg'] = "Mapping Failed (BW)"
                        for p_node, amt in self.mapped_nodes.values():
                            self.substrate.nodes[p_node]['cpu'] += amt

                    self._reset_request_state()

        return self._get_obs(), reward, done, False, info

    def _reset_request_state(self):
        self.current_alts = GraphGenerator.generate_request_alternatives()
        self.mapping_phase = False
        self.selected_alt_idx = None
        self.current_v_node_idx = 0
        self.mapped_nodes = {}

