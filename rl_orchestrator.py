def main():
    log_dir = "./tmp/"
    os.makedirs(log_dir, exist_ok=True)
    # Fix: safely bypass Monitor for action_masks
    env = ActionMasker(Monitor(VNEAPEnv(), log_dir), lambda env: env.unwrapped.action_masks())

    # 1. Train
    print(f"Training RL Agent for {TRAINING_STEPS} steps...")
    model = MaskablePPO("MultiInputPolicy", env, policy_kwargs=dict(features_extractor_class=VNEAPFeatureExtractor), verbose=0)
    model.learn(total_timesteps=TRAINING_STEPS)
    print("Training Complete.")

    # 2. Compare & Collect Data
    print(f"\nEvaluating Agents (Target Requests={TARGET_REQS})...")
    results = {}

    # RL
    rl_acc, rl_tot, rl_cpu, rl_bw = run_evaluation(env, model, target_requests=TARGET_REQS)
    results['RL'] = {'acc': rl_acc, 'tot': rl_tot, 'cpu': rl_cpu, 'bw': rl_bw}

    # Greedy
    greedy = GreedyAgent("Greedy")
    gr_acc, gr_tot, gr_cpu, gr_bw = run_evaluation(env, greedy, target_requests=TARGET_REQS)
    results['Greedy'] = {'acc': gr_acc, 'tot': gr_tot, 'cpu': gr_cpu, 'bw': gr_bw}

    # Tanto
    tanto = TantoProxyAgent("Tanto")
    ta_acc, ta_tot, ta_cpu, ta_bw = run_evaluation(env, tanto, target_requests=TARGET_REQS)
    results['Tanto'] = {'acc': ta_acc, 'tot': ta_tot, 'cpu': ta_cpu, 'bw': ta_bw}

    # 3. Plotting
    print("\nGenerating Plots...")
    names = list(results.keys())

    # Plot A: Acceptance Count
    accs = [results[n]['acc'] for n in names]
    plt.figure(figsize=(8, 4))
    plt.bar(names, accs, color=['blue', 'orange', 'green'])
    plt.title(f"Requests Accepted (Target Attempts={TARGET_REQS})")
    plt.ylabel("Count")
    plt.ylim(0, TARGET_REQS)
    plt.show()

    # Plot B: Rejection Rate
    rej_rates = []
    for n in names:
        tot = results[n]['tot']
        acc = results[n]['acc']
        rate = ((tot - acc) / tot * 100) if tot > 0 else 0
        rej_rates.append(rate)

    plt.figure(figsize=(8, 4))
    plt.bar(names, rej_rates, color=['red', 'red', 'red'], alpha=0.6)
    plt.title(f"Rejection Rate %")
    plt.ylabel("Rejection %")
    plt.ylim(0, 100)
    plt.show()

    # Plot C: Utilization Comparison
    fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(10, 8))

    # CPU
    for name in names:
        ax1.plot(results[name]['cpu'], label=name, alpha=0.8)
    ax1.set_title("Substrate CPU Utilization Over Time")
    ax1.set_ylabel("% Used")
    ax1.legend()

    # BW
    for name in names:
        ax2.plot(results[name]['bw'], label=name, alpha=0.8)
    ax2.set_title("Substrate Link Bandwidth Utilization Over Time")
    ax2.set_xlabel("Simulation Steps")
    ax2.set_ylabel("% Used")
    ax2.legend()

    plt.tight_layout()
    plt.show()

    # Plot D: Training Curve (Smoothed)
    try:
        x, y = ts2xy(load_results(log_dir), 'timesteps')
        if len(x) > 0:
            window = 50
            if len(y) >= window:
                y_smooth = np.convolve(y, np.ones(window)/window, mode='valid')
                x_smooth = x[len(x)-len(y_smooth):]
            else:
                x_smooth, y_smooth = x, y

            plt.figure(figsize=(8, 4))
            plt.plot(x_smooth, y_smooth, color='purple')
            plt.title("RL Training Reward (Smoothed)")
            plt.xlabel("Timesteps")
            plt.ylabel("Episode Reward")
            plt.show()
    except Exception as e:
        print(f"Could not plot training curve: {e}")

if __name__ == "__main__":
    main()

