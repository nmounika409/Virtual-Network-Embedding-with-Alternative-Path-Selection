# 4. HEURISTICS
# ==========================================
class HeuristicAgent:
    def __init__(self, name): self.name = name
    def predict(self, env):
        if not env.mapping_phase: return self.select_alternative(env)
        else: return self.map_node(env)
    def map_node(self, env):
        needed = env.current_alts[env.selected_alt_idx].nodes[env.current_v_node_idx]['cpu']
        mapped_p_nodes = [val[0] if isinstance(val, tuple) else val for val in env.mapped_nodes.values()]
        for i in range(SUBSTRATE_NODES):
             if env.substrate.nodes[i]['cpu'] >= needed and i not in mapped_p_nodes:
                 return i
        return 0
    def select_alternative(self, env): raise NotImplementedError

class GreedyAgent(HeuristicAgent):
    def select_alternative(self, env):
        best_idx, min_cost = 0, float('inf')
        for i, alt in enumerate(env.current_alts):
            cost = sum(nx.get_node_attributes(alt, 'cpu').values()) + sum(nx.get_edge_attributes(alt, 'bw').values())
            if cost < min_cost:
                min_cost = cost
                best_idx = i
        return best_idx

class TantoProxyAgent(HeuristicAgent):
    """
    TANTO (Proxy): Resource-Aware Load Balancing.
    Logic: Checks network scarcity (CPU vs BW) and penalizes the heavily used resource.
    """
    def select_alternative(self, env):
        # 1. Determine Network State
        total_cpu_cap = env.total_cpu
        total_bw_cap = env.total_bw

        current_cpu_used = total_cpu_cap - sum(nx.get_node_attributes(env.substrate, 'cpu').values())
        current_bw_used = total_bw_cap - sum(nx.get_edge_attributes(env.substrate, 'bw').values())

        # Calculate global utilization (0.0 to 1.0)
        cpu_util = current_cpu_used / total_cpu_cap
        bw_util = current_bw_used / (total_bw_cap if total_bw_cap > 0 else 1)

        best_idx = 0
        best_score = float('-inf')

        for i, alt in enumerate(env.current_alts):
            alt_cpu = sum(nx.get_node_attributes(alt, 'cpu').values())
            alt_bw = sum(nx.get_edge_attributes(alt, 'bw').values())

            # Score = Negative Weighted Demand.
            # We want to MINIMIZE demand on the stressed resource.
            score = - (cpu_util * alt_cpu + bw_util * alt_bw)

            if score > best_score:
                best_score = score
                best_idx = i

        return best_idx

