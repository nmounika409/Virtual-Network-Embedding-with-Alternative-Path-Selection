# 5. FEATURE EXTRACTOR & UTILS
# ==========================================
class VNEAPFeatureExtractor(BaseFeaturesExtractor):
    def __init__(self, observation_space, features_dim=64):
        super().__init__(observation_space, features_dim)
        self.sub_encoder = nn.Sequential(nn.Linear(SUBSTRATE_NODES, 32), nn.ReLU())
        self.req_encoder = nn.Sequential(nn.Linear(NUM_ALTERNATIVES * MAX_VNR_NODES, 32), nn.ReLU())
        self.final_layer = nn.Linear(64, features_dim)

    def forward(self, observations):
        sub_emb = self.sub_encoder(observations['substrate_cpu'])
        req_emb = self.req_encoder(observations['request_state'].flatten(start_dim=1))
        return self.final_layer(torch.cat([sub_emb, req_emb], dim=1))

def run_evaluation(env, agent, target_requests=200):
    """
    Runs until exactly 'target_requests' have been processed (Accepted or Failed).
    Ensures fair quantitative comparison between agents with different speeds.
    """
    # 1. Seed for fair start
    obs, _ = env.reset(seed=42)

    total_reward = 0
    accepted_requests = 0
    processed_requests = 0

    cpu_util_history = []
    bw_util_history = []

    # Safety break to prevent infinite loops if agent gets stuck
    max_steps = target_requests * 15
    steps = 0

    while processed_requests < target_requests and steps < max_steps:
        steps += 1

        # --- Predict ---
        if isinstance(agent, MaskablePPO):
             action, _ = agent.predict(obs, action_masks=env.action_masks(), deterministic=True)
        else:
             if not env.unwrapped.mapping_phase:
                 action = agent.select_alternative(env.unwrapped)
             else:
                 action = agent.map_node(env.unwrapped)

        # --- Step ---
        obs, reward, done, _, info = env.step(action)
        total_reward += reward

        # --- Track Request Completion ---
        msg = info.get('msg', '')
        if msg in ["Fully Embedded", "Mapping Failed (CPU)", "Mapping Failed (BW)"]:
            processed_requests += 1
            if msg == "Fully Embedded":
                accepted_requests += 1

        # --- Record Stats ---
        u_cpu, u_bw = env.unwrapped.get_utilization()
        cpu_util_history.append(u_cpu)
        bw_util_history.append(u_bw)

    return accepted_requests, processed_requests, cpu_util_history, bw_util_history

